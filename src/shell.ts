function addNormals(points:Array<vec3>):Float32Array
{
    var toReturn = new Float32Array(points.length*6)

    var index = 0;
    points.forEach(e=>{
        toReturn[index++] = e[0]
        toReturn[index++] = e[1]
        toReturn[index++] = e[2]
        toReturn[index++] = e[0]
        toReturn[index++] = e[1]
        toReturn[index++] = e[2]
    })
    return toReturn;
}


